public class compoundAssignment {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        int c = 3;
        int d = 4;

        a += 5;
        b -= 4;
        c *= 3;
        d /= 2;

        System.out.println("A = " + a);
        System.out.println("B = " + b);
        System.out.println("C = " + c);
        System.out.println("D = " + d);
    }
}

// Output:-
// A = 6
// B = -2
// C = 9
// D = 2